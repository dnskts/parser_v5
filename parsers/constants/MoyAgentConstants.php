    /**
     * Классы обслуживания: код → название.
     */
    public static function getCabinClassMap()
{
    $groups = array(
        'E,Y,B,H,K,M,L,V,Q,N,O,G,X,U' => 'ECONOMY',
        'J,C,D,Z,I' => 'BUSINESS',
        'F,P,R' => 'FIRST',
        'W' => 'PREMIUM ECONOMY',
        'A' => 'ALL CLASSES',
    );
    $map = array();
    foreach ($groups as $codes => $name) {
        foreach (explode(',', $codes) as $code) {
            $map[trim($code)] = $name;
        }
    }
    $map[''] = '';
    return $map;
}

