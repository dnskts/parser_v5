<?php
/**
 * ============================================================
 * ИНТЕРФЕЙС ПАРСЕРА ПОСТАВЩИКА
 * ============================================================
 * 
 * Этот файл описывает "контракт" — набор правил, которым должен
 * следовать каждый парсер поставщика. Если вы создаёте новый парсер,
 * он обязательно должен реализовать все методы, описанные ниже.
 * 
 * Благодаря этому интерфейсу система может работать с любым
 * количеством поставщиков одинаковым образом — ей не нужно знать,
 * как именно устроен XML каждого поставщика. Главное, чтобы парсер
 * умел преобразовать свой XML в единый JSON-формат.
 * ============================================================
 */

interface ParserInterface
{
    /**
     * Возвращает имя папки поставщика.
     * 
     * Это имя используется для поиска входных XML-файлов в папке input/.
     * Например, если метод возвращает "moyagent", система будет искать
     * XML-файлы в папке input/moyagent/
     * 
     * @return string — имя папки (латиницей, без пробелов)
     */
    public function getSupplierFolder();

    /**
     * Возвращает человекочитаемое название поставщика.
     * 
     * Используется для отображения в логах и интерфейсе.
     * Например: "Мой агент", "Travelport", "Amadeus"
     * 
     * @return string — название поставщика
     */
    public function getSupplierName();

    /**
     * Основной метод — парсинг XML-файла.
     * 
     * Принимает путь к XML-файлу поставщика, читает его,
     * извлекает данные и возвращает массив в едином формате заказа (ORDER).
     * 
     * Формат возвращаемого массива соответствует спецификации RSTLS:
     * [
     *   'UID'            => '...',           // Уникальный идентификатор заказа (UUID)
     *   'INVOICE_NUMBER' => '...',           // Номер заказа
     *   'INVOICE_DATA'   => '20251013121600', // Дата заказа (ГГГГММДДччммсс)
     *   'CLIENT'         => '...',           // Идентификатор клиента
     *   'PRODUCTS'       => [...]            // Массив продуктов (билеты, отели и т.д.)
     * ]
     * 
     * Если в файле ошибка, метод должен выбросить исключение (Exception)
     * с понятным описанием проблемы.
     * 
     * @param string $xmlFilePath — полный путь к XML-файлу
     * @return array — массив с данными заказа в едином формате
     * @throws Exception — если файл не удалось обработать
     */
    public function parse($xmlFilePath);
}
